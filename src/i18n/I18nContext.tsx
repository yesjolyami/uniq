type I18nContextValue = {
  locale: Locale;
  language: Language;
  setLocale: (locale: Locale) => void;
  t: (text: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = window.localStorage.getItem('unique-asia-locale') as Locale | null;
    return languages.some((language) => language.code === saved) ? saved! : 'ru';
  });

  const setLocale = (nextLocale: Locale) => {
    setLocaleState(nextLocale);
    window.localStorage.setItem('unique-asia-locale', nextLocale);
  };

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    language: languages.find((item) => item.code === locale) ?? languages[0],
    setLocale,
    t: (text) => (locale === 'ru' ? text : extendedTranslations[locale][text] ?? translations[locale][text] ?? text),
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used inside I18nProvider');
  return context;
}
